package ex1;

public class Calcul {

	/**
	 * <p>Donne la somme de l'addition de a par b</p>
	 * @param a première valeur
	 * @param b deuxième valeur
	 * @return	Somme de a par b
	 */
	public int addition(int a, int b){
		return a+b;
	}

	/**
	 * <p>Donne la somme de la soustraction de a par b</p>
	 * @param a première valeur
	 * @param b deuxième valeur
	 * @return	Différence de a par b
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
